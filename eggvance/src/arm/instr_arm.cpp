void ARM::Arm_BranchExchange(u32 instr)
{
    uint rn = bits<0, 4>(instr);

    pc = regs[rn];

    if (cpsr.t = pc & 0x1)
    {
        flushHalf();
        updateDispatch();
    }
    else
    {
        flushWord();
    }
}

template<uint link>
void ARM::Arm_BranchLink(u32 instr)
{
    uint offset = bits<0, 24>(instr);

    offset = signExtend<24>(offset);
    offset <<= 2;

    if (link) lr = pc - 4;

    pc += offset;
    flushWord();
}

template<uint flags_, uint opcode, uint imm_op>
void ARM::Arm_DataProcessing(u32 instr)
{
    enum class Opcode
    {
        And = 0b0000,
        Eor = 0b0001,
        Sub = 0b0010,
        Rsb = 0b0011,
        Add = 0b0100,
        Adc = 0b0101,
        Sbc = 0b0110,
        Rsc = 0b0111,
        Tst = 0b1000,
        Teq = 0b1001,
        Cmp = 0b1010,
        Cmn = 0b1011,
        Orr = 0b1100,
        Mov = 0b1101,
        Bic = 0b1110,
        Mvn = 0b1111
    };

    uint rd    = bits<12, 4>(instr);
    uint rn    = bits<16, 4>(instr);
    uint flags = flags_ && rd != 15;

    u32& dst = regs[rd];
    u32  op1 = regs[rn];
    u32  op2;

    if (imm_op)
    {
        uint value  = bits<0, 8>(instr);
        uint amount = bits<8, 4>(instr);
        op2 = util::rorArm<false>(value, amount << 1, flags, cpsr);
    }
    else
    {
        uint rm     = bits<0, 4>(instr);
        uint reg_op = bits<4, 1>(instr);
        uint shift  = bits<5, 2>(instr);

        op2 = regs[rm];

        if (reg_op)
        {
            uint rs = bits<8, 4>(instr);

            if (rn == 15) op1 += 4;
            if (rm == 15) op2 += 4;

            uint amount = regs[rs] & 0xFF;

            switch (static_cast<Shift>(shift))
            {
            case Shift::Lsl: op2 = util::lslArm(op2, amount, flags, cpsr); break;
            case Shift::Lsr: op2 = util::lsrArm<false>(op2, amount, flags, cpsr); break;
            case Shift::Asr: op2 = util::asrArm<false>(op2, amount, flags, cpsr); break;
            case Shift::Ror: op2 = util::rorArm<false>(op2, amount, flags, cpsr); break;

            default:
                EGG_UNREACHABLE;
                break;
            }
            idle();
        }
        else
        {
            uint amount = bits<7, 5>(instr);

            switch (static_cast<Shift>(shift))
            {
            case Shift::Lsl: op2 = util::lslArm(op2, amount, flags, cpsr); break;
            case Shift::Lsr: op2 = util::lsrArm<true>(op2, amount, flags, cpsr); break;
            case Shift::Asr: op2 = util::asrArm<true>(op2, amount, flags, cpsr); break;
            case Shift::Ror: op2 = util::rorArm<true>(op2, amount, flags, cpsr); break;

            default:
                EGG_UNREACHABLE;
                break;
            }
        }
    }

    switch (static_cast<Opcode>(opcode))
    {
    case Opcode::And: dst = util::log(op1 &  op2, flags, cpsr); break;
    case Opcode::Eor: dst = util::log(op1 ^  op2, flags, cpsr); break;
    case Opcode::Orr: dst = util::log(op1 |  op2, flags, cpsr); break;
    case Opcode::Mov: dst = util::log(       op2, flags, cpsr); break;
    case Opcode::Bic: dst = util::log(op1 & ~op2, flags, cpsr); break;
    case Opcode::Mvn: dst = util::log(      ~op2, flags, cpsr); break;
    case Opcode::Tst:       util::log(op1 &  op2,        cpsr); break;
    case Opcode::Teq:       util::log(op1 ^  op2,        cpsr); break;
    case Opcode::Cmn:       util::add(op1,   op2,        cpsr); break;
    case Opcode::Cmp:       util::sub(op1,   op2,        cpsr); break;
    case Opcode::Add: dst = util::add(op1,   op2, flags, cpsr); break;
    case Opcode::Adc: dst = util::adc(op1,   op2, flags, cpsr); break;
    case Opcode::Sub: dst = util::sub(op1,   op2, flags, cpsr); break;
    case Opcode::Sbc: dst = util::sbc(op1,   op2, flags, cpsr); break;
    case Opcode::Rsb: dst = util::sub(op2,   op1, flags, cpsr); break;
    case Opcode::Rsc: dst = util::sbc(op2,   op1, flags, cpsr); break;

    default:
        EGG_UNREACHABLE;
        break;
    }

    if (rd == 15)
    {
        if (flags_)
        {
            PSR spsr = this->spsr;
            switchMode(spsr.m);
            cpsr = spsr;
        }

        if (cpsr.t)
        {
            flushHalf();
            updateDispatch();
        }
        else
        {
            flushWord();
        }
    }
}

template<uint write, uint use_spsr, uint imm_op>
void ARM::Arm_StatusTransfer(u32 instr)
{
    if (write)
    {
        u32 op;
        if (imm_op)
        {
            uint value  = bits<0, 8>(instr);
            uint amount = bits<8, 4>(instr);
            op = rotateRight(value, amount << 1);
        }
        else
        {
            uint rm = bits<0, 4>(instr);
            op = regs[rm];
        }

        u32 mask = 0;
        if (instr & (1 << 19)) mask |= 0xFF00'0000;
        if (instr & (1 << 18)) mask |= 0x00FF'0000;
        if (instr & (1 << 17)) mask |= 0x0000'FF00;
        if (instr & (1 << 16)) mask |= 0x0000'00FF;

        if (use_spsr)
        {
            spsr = (spsr & ~mask) | (op & mask);
        }
        else
        {
            if (mask & 0xFF)
                switchMode(static_cast<PSR::Mode>(op & 0x1F));

            cpsr = (cpsr & ~mask) | (op & mask);
        }
    }
    else
    {
        uint rd = bits<12, 4>(instr);
        regs[rd] = use_spsr ? spsr : cpsr;
    }
}

template<uint flags, uint accumulate>
void ARM::Arm_Multiply(u32 instr)
{
    uint rm = bits< 0, 4>(instr);
    uint rs = bits< 8, 4>(instr);
    uint rn = bits<12, 4>(instr);
    uint rd = bits<16, 4>(instr);

    u32  op1 = regs[rm];
    u32  op2 = regs[rs];
    u32  op3 = regs[rn];
    u32& dst = regs[rd];

    dst = op1 * op2;

    if (accumulate)
    {
        dst += op3;
        idle();
    }

    if (flags)
        util::log(dst, cpsr);

    booth(op2, true);
}

template<uint flags, uint accumulate, uint sign>
void ARM::Arm_MultiplyLong(u32 instr)
{
    uint rm  = bits< 0, 4>(instr);
    uint rs  = bits< 8, 4>(instr);
    uint rdl = bits<12, 4>(instr);
    uint rdh = bits<16, 4>(instr);

    u64  op1  = regs[rm];
    u64  op2  = regs[rs];
    u32& dstl = regs[rdl];
    u32& dsth = regs[rdh];

    if (sign)
    {
        op1 = signExtend<32>(op1);
        op2 = signExtend<32>(op2);
    }

    u64 res = op1 * op2;

    if (accumulate)
    {
        res += (static_cast<u64>(dsth) << 32) | dstl;
        idle();
    }

    if (flags)
    {
        cpsr.z = res == 0;
        cpsr.n = res >> 63;
    }

    dstl = static_cast<u32>(res);
    dsth = static_cast<u32>(res >> 32);

    booth(static_cast<u32>(op2), sign);
}

template<uint load, uint writeback, uint byte, uint increment, uint pre_index, uint imm_op>
void ARM::Arm_SingleDataTransfer(u32 instr)
{
    uint rd = bits<12, 4>(instr);
    uint rn = bits<16, 4>(instr);

    u32& dst = regs[rd];
    u32 addr = regs[rn];

    u32 offset;
    if (imm_op)
    {
        offset = bits<0, 12>(instr);
    }
    else
    {
        uint rm     = bits<0, 4>(instr);
        uint reg_op = bits<4, 1>(instr);
        uint shift  = bits<5, 2>(instr);

        uint amount;
        if (reg_op)
        {
            uint rs = bits<8, 4>(instr);
            amount = regs[rs] & 0xFF;
        }
        else
        {
            amount = bits<7, 5>(instr);
        }

        switch (static_cast<Shift>(shift))
        {
        case Shift::Lsl: offset = util::lslArm(regs[rm], amount, false, cpsr); break;
        case Shift::Lsr: offset = util::lsrArm<true>(regs[rm], amount, false, cpsr); break;
        case Shift::Asr: offset = util::asrArm<true>(regs[rm], amount, false, cpsr); break;
        case Shift::Ror: offset = util::rorArm<true>(regs[rm], amount, false, cpsr); break;

        default:
            EGG_UNREACHABLE;
            break;
        }
    }

    if (!increment)
        offset = -offset;

    if (pre_index)
        addr += offset;

    if (load)
    {
        dst = byte
            ? readByte(addr)
            : readWordRotated(addr);

        if (rd == 15)
            flushWord();

        idle();
    }
    else
    {
        u32 value = (rd == 15)
            ? dst + 4
            : dst + 0;

        if (byte)
            writeByte(addr, value);
        else
            writeWord(addr, value);
    }

    if (writeback && (!load || rd != rn))
    {
        if (!pre_index)
            addr += offset;

        regs[rn] = addr;
    }
}

template<uint opcode, uint load, uint writeback, uint imm_op, uint increment, uint pre_index>
void ARM::Arm_HalfSignedDataTransfer(u32 instr)
{
    enum class Opcode
    {
        Swap  = 0b00,
        Ldrh  = 0b01,
        Ldrsb = 0b10,
        Ldrsh = 0b11
    };

    static_assert(static_cast<Opcode>(opcode) != Opcode::Swap);

    uint rd = bits<12, 4>(instr);
    uint rn = bits<16, 4>(instr);

    u32& dst = regs[rd];
    u32 addr = regs[rn];

    u32 offset;
    if (imm_op)
    {
        uint lower = bits<0, 4>(instr);
        uint upper = bits<8, 4>(instr);
        offset = (upper << 4) | lower;
    }
    else
    {
        uint rm = bits<0, 4>(instr);
        offset = regs[rm];
    }

    if (!increment)
        offset = -offset;

    if (pre_index)
        addr += offset;

    if (load)
    {
        switch (static_cast<Opcode>(opcode))
        {
        case Opcode::Ldrh:
            dst = readHalfRotated(addr);
            break;

        case Opcode::Ldrsb:
            dst = readByte(addr);
            dst = signExtend<8>(dst);
            break;

        case Opcode::Ldrsh:
            dst = readHalfSigned(addr);
            break;

        default:
            EGG_UNREACHABLE;
            break;
        }

        if (rd == 15)
            flushWord();

        idle();
    }
    else
    {
        u32 value = (rd == 15)
            ? dst + 4
            : dst + 0;

        writeHalf(addr, value);
    }

    if (writeback && (!load || rd != rn))
    {
        if (!pre_index)
            addr += offset;

        regs[rn] = addr;
    }
}

template<uint load, uint writeback_, uint user_mode, uint increment, uint pre_index_>
void ARM::Arm_BlockDataTransfer(u32 instr)
{
    uint rlist     = bits< 0, 16>(instr);
    uint rn        = bits<16,  4>(instr);
    uint writeback = writeback_;
    uint pre_index = pre_index_;

    u32 addr = regs[rn];
    u32 base = regs[rn];

    PSR::Mode mode = cpsr.m;
    if (user_mode)
        switchMode(PSR::Mode::Usr);

    if (rlist != 0)
    {
        if (!increment)
        {
            addr -= 4 * popcount(rlist);
            pre_index ^= 0x1;

            if (writeback)
            {
                regs[rn] = addr;
                writeback = false;
            }
        }

        if (load)
        {
            if (rlist & (1 << rn))
                writeback = false;

            for (uint x : SetBits(rlist))
            {
                addr += 4 * pre_index;
                regs[x] = readWord(addr);
                addr += 4 * pre_index ^ 0x4;
            }

            if (rlist & (1 << 15))
                flushWord();

            idle();
        }
        else
        {
            bool begin = true;

            for (uint x : SetBits(rlist))
            {
                u32 value = x != rn
                    ? x != 15
                        ? regs[x] + 0
                        : regs[x] + 4
                    : begin
                        ? base
                        : base + (increment ? 4 : -4) * popcount(rlist);

                addr += 4 * pre_index;
                writeWord(addr, value);
                addr += 4 * pre_index ^ 0x4;

                begin = false;
            }
        }
    }
    else
    {
        if (load)
        {
            pc = readWord(addr);
            flushWord();
        }
        else
        {
            enum class Suffix
            {
                DA = 0b00,
                DB = 0b01,
                IA = 0b10,
                IB = 0b11
            };

            switch (static_cast<Suffix>((increment << 1) | pre_index))
            {
            case Suffix::DA: writeWord(addr - 0x3C, pc + 4); break;
            case Suffix::DB: writeWord(addr - 0x40, pc + 4); break;
            case Suffix::IA: writeWord(addr + 0x00, pc + 4); break;
            case Suffix::IB: writeWord(addr + 0x04, pc + 4); break;

            default:
                EGG_UNREACHABLE;
                break;
            }
        }

        addr = increment
            ? addr + 0x40
            : addr - 0x40;
    }

    if (writeback)
        regs[rn] = addr;

    if (user_mode)
        switchMode(mode);
}

template<uint byte>
void ARM::Arm_SingleDataSwap(u32 instr)
{
    uint rm = bits< 0, 4>(instr);
    uint rd = bits<12, 4>(instr);
    uint rn = bits<16, 4>(instr);

    u32  src = regs[rm];
    u32& dst = regs[rd];
    u32 addr = regs[rn];

    if (byte)
    {
        dst = readByte(addr);
        writeByte(addr, src);
    }
    else
    {
        dst = readWordRotated(addr);
        writeWord(addr, src);
    }
    idle();
}

